    static long coins(int x){
        if(x<0)
            return Integer.MAX_VALUE;
        if(x == 0)
            return 0;

        if(dp.containsKey(x))
            return dp.get(x);
        long best = Integer.MAX_VALUE;
        for(int c: coins){
            best = Math.min(best, coins(x-c)+1);
        }
        dp.put(x, best);
        return best;
    }
